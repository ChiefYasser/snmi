const FR = {};
function snapshotFR() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    FR[key] = el.tagName === "META" ? el.getAttribute("content") : el.innerHTML;
  });
}
function applyLang(lang) {
  const dict = lang === "en" ? EN : FR;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] === undefined) return;
    if (el.tagName === "META") el.setAttribute("content", dict[key]);
    else el.innerHTML = dict[key];
  });
  document.documentElement.lang = lang;
  document.querySelectorAll(".lang-opt").forEach(o => {
    const on = o.dataset.lang === lang;
    o.classList.toggle("text-ink", on);
    o.classList.toggle("text-stone", !on);
  });
  localStorage.setItem("snmi-lang", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  // Language is now served as separate pages (/ and /en/) for SEO;
  // snapshotFR/applyLang are retained only as a utility, not auto-run.
  void snapshotFR; void applyLang;

  const header = document.getElementById("header");
  const progress = document.getElementById("progress");
  const toTop = document.getElementById("toTop");
  const reduce = matchMedia("(prefers-reduced-motion: reduce)").matches;

  // header transparent over hero, solid after
  const hero = document.getElementById("hero");
  function onScroll() {
    const h = document.documentElement;
    const max = h.scrollHeight - h.clientHeight;
    const pct = max > 0 ? (h.scrollTop / max) * 100 : 0;
    progress.style.width = pct + "%";
    const threshold = hero ? hero.offsetHeight - 90 : 60;
    const atTop = h.scrollTop < threshold;
    header.classList.toggle("at-top", atTop);
    header.classList.toggle("shadow-md", !atTop);
    toTop.classList.toggle("show", h.scrollTop > 600);
  }
  addEventListener("scroll", onScroll, { passive: true });
  onScroll();
  toTop.addEventListener("click", () => scrollTo({ top: 0, behavior: reduce ? "auto" : "smooth" }));

  // mobile menu
  const burger = document.getElementById("burger");
  const mobileNav = document.getElementById("mobileNav");
  burger.addEventListener("click", () => mobileNav.classList.toggle("hidden"));
  mobileNav.querySelectorAll("a").forEach(a => a.addEventListener("click", () => mobileNav.classList.add("hidden")));

  // Reveal + stagger: enhance already-visible content (never gated on JS).
  const revealEls = document.querySelectorAll(".will-reveal, .stagger");
  if (reduce) {
    revealEls.forEach(el => el.classList.add("in"));
  } else {
    const obs = new IntersectionObserver((entries) => {
      entries.forEach((e) => {
        if (e.isIntersecting) { e.target.classList.add("in"); obs.unobserve(e.target); }
      });
    }, { threshold: 0.12, rootMargin: "0px 0px -8% 0px" });
    revealEls.forEach(el => obs.observe(el));
  }

  // Animated stat counters (run once when hero stats enter view)
  const counters = document.querySelectorAll("[data-count]");
  function runCount(el) {
    const target = +el.dataset.count;
    const suffix = el.dataset.suffix || "";
    if (reduce) { el.innerHTML = target + suffix; return; }
    const dur = 1100; const start = performance.now();
    function tick(now) {
      const p = Math.min((now - start) / dur, 1);
      const eased = 1 - Math.pow(1 - p, 3);
      el.innerHTML = Math.round(target * eased) + (p === 1 ? suffix : "");
      if (p < 1) requestAnimationFrame(tick);
    }
    requestAnimationFrame(tick);
  }
  if (counters.length) {
    const cObs = new IntersectionObserver((entries) => {
      entries.forEach(e => { if (e.isIntersecting) { runCount(e.target); cObs.unobserve(e.target); } });
    }, { threshold: 0.6 });
    counters.forEach(c => cObs.observe(c));
  }

  // Scrollspy: highlight the active nav link
  const navLinks = document.querySelectorAll("#nav .nav-link");
  const sections = [...navLinks].map(a => document.querySelector(a.getAttribute("href"))).filter(Boolean);
  if (sections.length) {
    const sObs = new IntersectionObserver((entries) => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          const id = e.target.id;
          navLinks.forEach(a => a.classList.toggle("active", a.getAttribute("href") === "#" + id));
        }
      });
    }, { rootMargin: "-45% 0px -50% 0px" });
    sections.forEach(s => sObs.observe(s));
  }

  document.getElementById("year").textContent = new Date().getFullYear();

  // Sticky image spy (sectors + services): change the sticky image as rows
  // are hovered/focused (desktop) or scroll into view (mobile/scroll).
  function imageSpy(stackId, spyAttr) {
    const stack = document.getElementById(stackId);
    if (!stack) return;
    const imgs = stack.querySelectorAll("img");
    const rows = document.querySelectorAll('[data-spy="' + spyAttr + '"] [data-i]');
    const set = (i) => {
      imgs.forEach(im => im.classList.toggle("active", +im.dataset.i === i));
      rows.forEach(r => r.classList.toggle("active", +r.dataset.i === i));
    };
    rows.forEach(r => {
      r.addEventListener("mouseenter", () => set(+r.dataset.i));
      r.addEventListener("focusin", () => set(+r.dataset.i));
    });
    if (!reduce) {
      const io = new IntersectionObserver((es) => {
        es.forEach(e => { if (e.isIntersecting) set(+e.target.dataset.i); });
      }, { rootMargin: "-45% 0px -45% 0px", threshold: 0 });
      rows.forEach(r => io.observe(r));
    }
  }
  imageSpy("sectorImg", "sector");
  imageSpy("serviceImg", "service");

  // References accordion: click a row to pin it open (hover opens via CSS).
  document.querySelectorAll(".ref-row").forEach(row => {
    const head = row.firstElementChild;
    head.addEventListener("click", () => row.classList.toggle("open"));
  });

  // Marquees: fill the viewport with enough copies so a rail is NEVER empty,
  // and translate by exactly one set width for a seamless loop on any screen.
  const marquees = document.querySelectorAll(".marquee");
  function buildMarquees() {
    marquees.forEach(m => {
      if (!m.dataset.set) m.dataset.set = m.firstElementChild.outerHTML; // canonical set (first group)
      m.innerHTML = m.dataset.set;
      const first = m.firstElementChild;
      const setW = first.getBoundingClientRect().width;
      if (!setW) return;
      // enough copies to cover viewport + one extra set (min 2)
      const copies = Math.max(2, Math.ceil((window.innerWidth + setW) / setW) + 1);
      for (let i = 1; i < copies; i++) {
        const c = first.cloneNode(true);
        c.setAttribute("aria-hidden", "true");
        m.appendChild(c);
      }
      const speed = m.classList.contains("slow") ? 70 : 95; // px per second
      m.style.setProperty("--mq-set", setW + "px");
      m.style.setProperty("--mq-dur", (setW / speed).toFixed(2) + "s");
    });
  }
  if (marquees.length) {
    buildMarquees();
    addEventListener("load", buildMarquees);          // re-measure once logos have loaded
    let rt; addEventListener("resize", () => { clearTimeout(rt); rt = setTimeout(buildMarquees, 200); });
  }
});
